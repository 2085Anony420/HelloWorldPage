function showPopup(popup1) {
    var popup = document.getElementById(popup1);
    if (popup) {
        popup.style.display = "block";
    }
}

function hidePopup(popup1) {
    var popup = document.getElementById(popup1);
    if (popup) {
        popup.style.display = "none";
    }
}

